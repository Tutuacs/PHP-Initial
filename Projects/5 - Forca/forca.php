function verifica_letra($letra){
    global $entrada;
    $tem = 0;
    $j=0;

    while($j<strlen($entrada)){
        if($letra[0] == $entrada[$j]){
            $tem = 1;
        }
	$j++;
    }

    return($tem);
}
